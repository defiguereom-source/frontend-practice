function sumar (a,b){
    return a + b;
}

//Los argumentos son los valores que se le pasan a una función para que esta pueda realizar su tarea. En este caso, la función sumar recibe dos argumentos, a y b, que son los números que se van a sumar. Al llamar a la función con sumar(2,3), estamos pasando los argumentos 2 y 3, y la función devuelve el resultado de la suma, que es 5.
console.log(sumar(2,9));
