// bit: 0 1
// 1 byte = 8 bits
// 1 kilobyte (KB) = 1024 bytes
// 1 megabyte (MB) = 1024 KB
// 1 gigabyte (GB) = 1024 MB
// 1 terabyte (TB) = 1024 GB
 
console.log(5 & 3); // AND
console.log(5 | 3); // OR
console.log(5 ^ 3); // XOR
console.log(~5); // NOT
console.log(5 << 1); // Left shift
console.log(5 >> 1); // Right shift 